	SUBROUTINE EBASE
        COMMON /DATA/Q(300,200),IPL(200),QMAX,QMIN,IS,IE,IPP,NPL,NT,
     1PRE(300),AT,QO(300),IOBS,IPR,ILIST,PA(300),PEF(300),NTT,PER(300),
     2PMAX,VOP(300),P(300,10)
	READ(1,3) AREA,XKSUB,QSUB0
	WRITE(2,5) QSUB0,XKSUB
	XK2=XKSUB*3600./AT
	XK2=EXP(-1/XK2)
	FATOR=AREA*1000/AT
	QSUB1=QSUB0/FATOR
	DO 10 J=1,NT
	QSUB1=QSUB1*XK2+VOP(J)*(1.-XK2)
	Q(J,IS)=Q(J,IS)+QSUB1*FATOR
10	CONTINUE
	RETURN
3	FORMAT(8F10.0)
5	FORMAT(//,10X,'V BASE INIC:',F20.2,' M3/SEG'/
     1	10X,'KSUB             :',F14.2,' HORAS')
	END
