        SUBROUTINE TCV
	DIMENSION NPU(10),COTH(10)
        COMMON /DATA/Q(300,200),IPL(200),QMAX,QMIN,IS,IE,IPP,NPL,NT,
     1PRE(300),AT,QO(300),IOBS,IPR,ILIST,PA(300),PEF(300),NTT,PER(300),
     2PMAX,VOP(300),P(300,10)
	DO 30 I=1,NTT
	PA(I)=0.
30	PRE(I)=0.
	READ(1,2) NTPU,LCHUVA
	READ(1,2) (NPU(I),I=1,NTPU)
	READ(1,3) (COTH(I),I=1,NTPU)
  	DO 20 J=1,NTPU
	DO 20 I=1,NTT
        KKK=NPU(J)
20	PRE(I)=PRE(I)+COTH(J)*P(I,KKK)
        PA(1)=PRE(1)
        DO 70 I=2,NTT
70      PA(I)=PA(I-1)+PRE(I)
        IF(LCHUVA.EQ.0)GO TO 200
        CALL CHUVAP
200     IF(NTT.GE.NT)GO TO 220
        DO 210 K=NTT+1,NT
        PA(K)=0.
        PEF(K)=0.
210     PRE(K)=0.
220     READ(1,2)NPER
        GO TO(300,400,500,600,700)NPER
300     CALL IPHII
        GO TO 800
400     CALL SCS
        GO TO 800
500     CALL HEC1
        GO TO 800
600     CALL FI
        GO TO 800
700     CALL HOLTAN
800     READ(1,2) NPRPC,NESCB
        GO TO (1000,1100,1200,1300)NPRPC
1000    CALL HU
        GO TO 2000
1100    CALL HIDT
        GO TO 2000
1200    CALL NASH
        GO TO 2000
1300    CALL CLARK
2000	IF(NESCB.NE.0) CALL EBASE
        IF (IPR.EQ.0)GO TO 3000
        CALL PLOTA(-1)
3000    IF (ILIST.EQ.0)GO TO 4000
        CALL HID
4000    RETURN
2       FORMAT(8I10)
3       FORMAT(8F10.0)
        END
